    private void beliBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beliBTActionPerformed
        // TODO add your handling code here:
        if (beliTF.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Anda belum memasukkan jumlah beli");
            beliTF.requestFocus();
        }
        else{
            try {
                Connection koneksikan=koneksi.getkoneksikan();
                Statement s=koneksikan.createStatement();
                String id=idTF.getText();
                String nama=namaTF.getText();
                String jumlah=beliTF.getText();
                String harga=hargaTF.getText();
                int a1,total,a2;
                a1 = Integer.parseInt(harga);
                a2 = Integer.parseInt(jumlah);
                total=a1*a2;
                String input="insert into keranjang1 values('"+id+"',"+"'"+nama+"',"+"'"+jumlah+"',"+"'"+total+"')";
                s.executeUpdate(input); 
                String pesan="Berhasil menambahkan buku di keranjang";
                JOptionPane.showMessageDialog(null, pesan,"Pemberitahuan",JOptionPane.INFORMATION_MESSAGE);
                beliBuku bl=new beliBuku();
                bl.setVisible(true);
                dispose();
            } catch (SQLException ex) {
                    System.err.println("SQLException: "+ex.getMessage());
        }
        }
    }//GEN-LAST:event_beliBTActionPerformed

