    private void cariLBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cariLBMouseClicked
        // TODO add your handling code here:\
        String cari=cariTF.getText();
        Connection koneksikan=koneksi.getkoneksikan();
        if (cari.equals("")){
                        JOptionPane.showMessageDialog(rootPane, "Masukkan keyword buku terlebih dahulu");
                        cariTF.requestFocus();
                        getData();
                    }
        else{
        try {
            int baris=model.getRowCount();
            for(int i=1;i<=baris;i++){
                model.removeRow(0);
            }
            Statement s=koneksikan.createStatement();
            String sql="SELECT *from data_buku where nama_buku like'%"+cari+"%'or id_buku like '%"+cari+"%'";
            ResultSet r=s.executeQuery(sql);
            while(r.next()){
                model.addRow(new String[]{
                    r.getString(1),
                    r.getString(2),
                    r.getString(3),
                    r.getString(4),
                    r.getString(5),
                    r.getString(6),
                    r.getString(7),
                    r.getString(8),
                });
            }
            bukuTB.setModel(model);
            cariTF.setText("");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Buku yang Anda cari tidak tersedia");
            System.err.println("error(search):"+ex);
        }}

    }//GEN-LAST:event_cariLBMouseClicked

